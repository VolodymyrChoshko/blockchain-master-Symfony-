/**
 * @param text
 * @return {*}
 */
const escapeRegExp = (text) => {
  return text.replace(/[-[\]{}()*+?.,\\^$|#\s]/g, '\\$&');
};

/**
 * @param text
 * @return {*}
 */
export const replaceClassicEmojis = (text) => {
  Object.keys(classicEmojis).forEach((key) => {
    text = text.replace(new RegExp(`(\\b|\\s|^)${escapeRegExp(key)}(\\b|\\s|$)`, 'g'), `$1${classicEmojis[key]}$2`);
  });

  return text;
};
